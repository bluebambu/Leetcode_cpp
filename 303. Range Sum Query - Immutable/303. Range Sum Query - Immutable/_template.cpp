class NumArray {
public:
	vector<int> dp; 

	NumArray(vector<int> &nums):dp(nums.size()+1, 0) {
		for (int i = 1; i < dp.size(); ++i){
			dp[i] = dp[i - 1] + nums[i - 1]; 
		}
	}

	int sumRange(int i, int j) {
		return dp[j+1] - dp[i]; // 减去 dp[i]， 而不是 dp[i+1]， 因为 nums[i]被包括在结果中 
	}
};


