struct TreeNode
{
    TreeNode(int a):val(a), left(nullptr), right(nullptr) {}
    TreeNode(int a, TreeNode* x, TreeNode* y):val(a), left(x), right(y) {}
    TreeNode():val(0), left(nullptr), right(nullptr) {}

    int val;
    TreeNode *left;
    TreeNode *right;

};


class Solution
{
public:
    int numTrees(int n) {
        vector<int> dp(n+1, 0);

        dp[0]=1;
        dp[1]=1;

        for(int i=2; i<n+1;++i){
            for(int k=1; k<n+1; ++k){
                dp[i]+=dp[k-1]*dp[i-k];
            }
        }

        return dp[n];

    }
};

